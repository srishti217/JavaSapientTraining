
public class AddTwoNumbers {

	public static void main(String[] args) {
		int i=30, j=20;
		System.out.println("Addition of two no is : "+(i+j));
		System.out.println("Subtraction of two no is : "+(i-j));
		System.out.println("Multiplication of two no is : "+(i*j));
		System.out.println("Quotient after division is : "+(i/j));
		System.out.println("Remainder after division is : "+(i%j));
	}

}
